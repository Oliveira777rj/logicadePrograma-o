/*O primeiro comentário deve ser colocado no início do arquivo indicando
o objetivo do mesmo através de um pequeno resumo. Ao criar um método, 
procure sempre informar qual é a
finalidade do mesmo e se ele irá retornar ou não alguma informação.
Em muitas variáveis criadas, 
somente através de seu nome nem sempre é possível saber que informação ela irá armazenar ou 
para que ela servirá, por isso, não deixe de comentá-la nesses casos. */

/* colocando textos curtos e somente aqueles que você achar relevante para a leitura e
 principalmente para a compreensão do código. E quando achar necessário colocar um 
 comentário para que outras pessoas consigam entender o algoritmo, procure rever o seu código. 
 Talvez melhorando ele, nem seja necessário colocar essas informações adicionais.*/ 

 /*Os comentários geralmente nos ajudam a explicar ou recordar de algo no código. Porém,
comentários em excesso não é algo muito legal… Se você está tendo que explicar tudo que
ocorre em seu código, é porque o código provavelmente está mal escrito ou bagunçado.
Sendo assim, tente sempre restringir os comentários aos trechos onde realmente seja necessário. 
Trechos de código que sigam práticas como a utilização de nomes significativos geralmente
auxiliam a restringir os comentários aos trechos onde os mesmos sejam de fato necessários.*/

/*Não use comentários para versionar o código
Utilize ferramentas apropriadas pra isso, se sua intenção é versionar o código,
 utilize um controle de versão como o Git ou Subversion, por exemplo.
 */